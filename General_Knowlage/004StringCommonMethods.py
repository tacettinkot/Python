name = "Bro Conner"

# To count Length of the string
print(len(name)) #technically this is a function
# find how many same letter in the string
print(name.find("o"))
# Capitalize the first letter of the String
print(name.capitalize())
# Make upper all characters
print(name.upper())
# Make lower all letters in String
print(name.lower())
# Checks all characters are number
print(name.isdigit())
# Checks all characters are letter
print(name.isalpha())
# How many that character in the that string
print(name.count("o"))
# replace all same characters
print(name.replace("o","a"))
# Type same string as much as need
print(name*3)