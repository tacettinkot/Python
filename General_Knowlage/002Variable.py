# variable = a container for a value. Behaves as the value that it contains
# We can store different data types in the variables like string int or boolean

# STRING VARIABLE

# "" or '' code makes our variable string
name = 'Taji'
print(name)
# We can concanate another string in print function
print("Hello " + name )
# If you need to learn what type of datatype we can check like fallowing option
print(type(name))      # name is str type short of string
first_name = "Taji"
last_name = "Jeans"
full_name = first_name + " " + last_name
print("Hi " + full_name)

# INTEGER INT VARIABLE
age = 35
age += 1       # or age = age + 1
print(age)
print(type(age))

# float = floating point number ( a decimal number)

height = 250.5
print(height)
print(type(height))

# Boolean true false situations
human = True
print(human)
