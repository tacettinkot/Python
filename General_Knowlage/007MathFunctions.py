import math

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi))
print(math.ceil(pi))  # ceil() round up the number
print(math.floor(pi))  # floor() round always down
print(abs(pi))  # abs() absalute number (always positive result)
print(pow(pi, 2))  # pow() power of
print(math.sqrt(420))  # sqrt() square root of
print(max(x, y, z))  # max() get the max number
print(min(x, y, z))  # min() gets the min number
