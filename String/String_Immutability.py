# String Immutability   
# We cannot change the string
# To change string, we should reassign the new(changed) 
# string value to a variable

var_string ='Clarusway'
print(var_string.lower())
print(var_string)
var_string = 'Clarusway'.lower()
print(var_string)

var_str = 'In God we Trust'
var_str.lower()
print(var_str)